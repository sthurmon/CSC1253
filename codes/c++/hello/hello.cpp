#include <iostream>             /// The iostream library is included to allow for input and output.
using namespace std;            /// The std, or standard, namespace is used before referencing objects within the iostream library, such as cout below.

int main() {                    /// The main function (which every C++ executable has) is defined, and contains the main instruction code to be executed.
  cout << "Hello, world!\n";    /// The string "Hello, world!" is printed to the screen. The "\n" indicates a new line. The cout keyword represents the terminal, and the << symbol indicates information flowing from the program to the terminal.
  return 0;                     /// An error code of 0 is returned to the user, and indicates the program exited successfully.
}
